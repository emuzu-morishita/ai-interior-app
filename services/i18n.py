def t(lang: str, key: str, **kwargs) -> str:
    """指定言語の文言を返す。未定義キーは日本語にフォールバック。"""
    table = TRANSLATIONS.get(lang, TRANSLATIONS["ja"])
    text = table.get(key) or TRANSLATIONS["ja"].get(key, key)
    return text.format(**kwargs) if kwargs else text
